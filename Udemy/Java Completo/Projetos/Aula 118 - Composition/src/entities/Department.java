package entities;

public class Department {

	private String name;
	
	//construtor vazio
	public Department() {
		
	}

	//construtor cheio
	public Department(String name) {
		this.name = name;
	}

	//Get e Set
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	

	
	
}
